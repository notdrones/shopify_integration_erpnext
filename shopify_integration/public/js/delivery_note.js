frappe.ui.form.on('Delivery Note', {
    refresh: function(frm) {
        frappe.xcall(
            'shopify_integration.utils.sales_invoice.get_dn_shopify_invoice_status',
            { dn_name: frm.doc.name }
        ).then(function(info) {
            if (!info || !info.is_shopify || info.has_si) return;

            const isSubmitted = frm.doc.docstatus === 1;
            let msg;

            if (!isSubmitted) {
                // Draft DN — inform the user what will happen on submit
                if (info.si_timing === 'Immediate') {
                    msg = __('<b>Shopify Order:</b> The Sales Invoice will be created automatically when this Delivery Note is submitted.');
                } else if (info.delay_hours > 0) {
                    msg = __('<b>Shopify Order:</b> The Sales Invoice will be auto-generated {0} hour(s) after submission by the Shopify scheduler.', [info.delay_hours]);
                } else {
                    msg = __('<b>Shopify Order:</b> The Sales Invoice will be auto-generated by the Shopify scheduler after submission.');
                }
            } else {
                // Submitted DN, SI not yet created — pending or failed
                if (info.si_timing === 'Immediate') {
                    msg = __('<b>Shopify Order:</b> Sales Invoice auto-creation may be pending or has failed.');
                } else if (info.delay_hours > 0) {
                    msg = __('<b>Shopify Order:</b> Sales Invoice will be auto-generated {0} hour(s) after submission.', [info.delay_hours]);
                } else {
                    msg = __('<b>Shopify Order:</b> Sales Invoice auto-creation is pending (next scheduler run).');
                }
            }

            frm.dashboard.clear_headline();
            frm.dashboard.set_headline_alert(
                '<div style="padding-right:40px;display:block;line-height:1.5;">' + msg + '</div>',
                'yellow'
            );
        });
    }
});
