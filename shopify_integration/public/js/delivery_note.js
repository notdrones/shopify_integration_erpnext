frappe.ui.form.on('Delivery Note', {
    refresh: function(frm) {
        _handle_shopify_si_banner(frm);
        _handle_shopify_fulfillment(frm);
    },

    // Re-run fulfillment UI when tracking fields change so the
    // "Update Tracking" button state stays accurate.
    shopify_tracking_number:  function(frm) { _handle_shopify_fulfillment(frm); },
    shopify_tracking_url:     function(frm) { _handle_shopify_fulfillment(frm); },
    shopify_tracking_company: function(frm) { _handle_shopify_fulfillment(frm); },
});


// ── Sales Invoice auto-creation banner ────────────────────────────────────────

function _handle_shopify_si_banner(frm) {
    // Single server call — checks all conditions and returns the info needed
    // to render the banner.  Avoids querying Sales Invoice Item (a child DocType)
    // directly through frappe.client, which throws a PermissionError.
    frappe.xcall(
        'shopify_integration.utils.sales_invoice.get_dn_shopify_invoice_status',
        { dn_name: frm.doc.name }
    ).then(function(info) {
        if (!info || !info.is_shopify || info.has_si) return;

        let msg;
        if (info.si_timing === 'Immediate') {
            msg = __('<b>Shopify Order:</b> The Sales Invoice for this Delivery Note will be created automatically upon submission. You do not need to create it manually.');
        } else if (info.delay_hours > 0) {
            msg = __('<b>Shopify Order:</b> The Sales Invoice will be automatically generated by the Shopify Integration scheduler {0} hour(s) after submission. You do not need to create it manually.', [info.delay_hours]);
        } else {
            msg = __('<b>Shopify Order:</b> The Sales Invoice for this Delivery Note will be automatically generated by the Shopify Integration scheduler. You do not need to create it manually.');
        }

        frm.dashboard.clear_headline();
        frm.dashboard.set_headline_alert(
            '<div style="padding-right:40px;display:block;line-height:1.5;">' + msg + '</div>',
            'yellow'
        );

        setTimeout(() => {
            if (frm.custom_buttons && frm.custom_buttons['Sales Invoice']) {
                frm.remove_custom_button('Sales Invoice', 'Create');
            }
        }, 100);
    });
}


// ── Fulfillment push UI ───────────────────────────────────────────────────────

function _handle_shopify_fulfillment(frm) {
    frappe.xcall(
        'shopify_integration.utils.fulfillment.get_dn_fulfillment_status',
        { dn_name: frm.doc.name }
    ).then(function(info) {
        if (!info || !info.is_shopify) return;

        const status   = info.fulfillment_status || 'Pending';
        const trigger  = info.fulfillment_trigger || 'On DN Submit';
        const id       = info.fulfillment_id || '';
        const error    = info.fulfillment_error || '';
        const docstatus = frm.doc.docstatus;

        // ── Status badge ──────────────────────────────────────────────────────
        _render_fulfillment_badge(frm, status, id, error);

        // ── Buttons (only on submitted DNs) ───────────────────────────────────
        if (docstatus !== 1) return;

        // "Push Fulfillment to Shopify" — shown when:
        //   • trigger is Manual and not yet fulfilled, OR
        //   • status is Failed (retry path for either trigger)
        const can_push = (
            (trigger === 'Manual' && status === 'Pending') ||
            status === 'Failed'
        );
        if (can_push) {
            frm.add_custom_button(
                status === 'Failed' ? __('Retry Fulfillment') : __('Push Fulfillment to Shopify'),
                function() { _push_fulfillment(frm); },
                __('Shopify')
            );
        }

        // "Push Remaining" — for Partially Fulfilled DNs
        if (status === 'Partially Fulfilled') {
            frm.add_custom_button(__('Push Remaining to Shopify'), function() {
                // Clear the existing fulfillment_id so the guard lets it retry
                frappe.confirm(
                    __('This will attempt to push the remaining unfulfilled items to Shopify. Continue?'),
                    function() { _push_fulfillment(frm); }
                );
            }, __('Shopify'));
        }

        // "Update Tracking" — shown after a fulfillment exists and tracking number is set
        if (id && frm.doc.shopify_tracking_number) {
            frm.add_custom_button(__('Update Tracking on Shopify'), function() {
                _update_tracking(frm);
            }, __('Shopify'));
        }

        // Warn if carrier is "Other" but no tracking URL provided
        if (
            frm.doc.shopify_tracking_company === 'Other' &&
            frm.doc.shopify_tracking_number  &&
            !frm.doc.shopify_tracking_url
        ) {
            frappe.show_alert({
                message: __('Carrier is set to "Other" but no Tracking URL is provided. Shopify cannot generate a URL for unlisted carriers — please enter the full tracking link.'),
                indicator: 'orange',
            }, 10);
        }
    });
}


// ── Fulfillment badge rendering ───────────────────────────────────────────────

function _render_fulfillment_badge(frm, status, fulfillment_id, error) {
    const colour_map = {
        'Pending':              'grey',
        'Fulfilled':            'green',
        'Partially Fulfilled':  'orange',
        'Failed':               'red',
        'Skipped':              'grey',
    };
    const colour = colour_map[status] || 'grey';

    let label = status;
    if (fulfillment_id && status === 'Fulfilled') {
        label = `Fulfilled · #${fulfillment_id}`;
    }
    if (status === 'Failed' && error) {
        label = `Failed: ${error.substring(0, 80)}${error.length > 80 ? '…' : ''}`;
    }

    // Use the form's indicator (top-left coloured dot + text)
    frm.page.set_indicator(
        __('Shopify: {0}', [label]),
        colour
    );
}


// ── Action helpers ────────────────────────────────────────────────────────────

function _push_fulfillment(frm) {
    frappe.show_alert({ message: __('Pushing fulfillment to Shopify…'), indicator: 'blue' });
    frappe.xcall(
        'shopify_integration.utils.fulfillment.manual_push_fulfillment',
        { dn_name: frm.doc.name }
    ).then(function(result) {
        if (result.success) {
            frappe.show_alert({ message: result.message, indicator: 'green' });
        } else {
            frappe.msgprint({
                title:     __('Fulfillment Failed'),
                message:   result.message,
                indicator: 'red',
            });
        }
        frm.reload_doc();
    });
}

function _update_tracking(frm) {
    frappe.show_alert({ message: __('Updating tracking on Shopify…'), indicator: 'blue' });
    frappe.xcall(
        'shopify_integration.utils.fulfillment.update_fulfillment_tracking',
        { dn_name: frm.doc.name }
    ).then(function(result) {
        frappe.show_alert({
            message:   result.message,
            indicator: result.success ? 'green' : 'red',
        }, 8);
    });
}
